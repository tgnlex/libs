function Add(x, y)
  local result = x + y;
  return result;
end

function Subtract(x, y)
  local result = x - y;
  return result;
end

function Multiply(x, y)
  local result = x * y;
  return result;
end

function Divide(x, y)
  local result = x / y;
  return result;
end

function Increment(x)
  local  result =  x + 1;
  return result;
end

function Decrement(x)
  local result = x - 1;
  return result;
end 

