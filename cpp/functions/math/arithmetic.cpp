int add(int x, int y) {
  int result;
  result = x + y;
  return result;
}
int subtract(int x, int y) {
  int result;
  result = x - y;
  return result;
}
int multiply(int x, int y) {
  int result;
  result = x * y; 
  return result;
}
int divide(int x, int y) {
  int result;
  result = x / y;
  return result;
}

